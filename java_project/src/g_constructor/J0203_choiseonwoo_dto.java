package g_constructor;

public class J0203_choiseonwoo_dto {
	String name, result;
	int kor, eng, mat, ave;
	
	public J0203_choiseonwoo_dto(String name, String result, int kor, int eng, int mat, int ave) {
		
		this.name = name;
		this.result = result;
		this.kor = kor;
		this.eng = eng;
		this.mat = mat;
		this.ave = ave;
	}

	public String getName() {
		return name;
	}

	public String getResult() {
		return result;
	}

	public int getKor() {
		return kor;
	}

	public int getEng() {
		return eng;
	}

	public int getMat() {
		return mat;
	}

	public int getAve() {
		return ave;
	}

}
	
	
	

	
	