package h_arraylist;

public class J0207_1_dto {
	String id, name, area;
	int age;
	public J0207_1_dto(String id, String name, String area, int age) {
		this.id = id;
		this.name = name;
		this.area = area;
		this.age = age;	
	}
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getArea() {
		return area;
	}
	public int getAge() {
		return age;
	}
	
	
	
}
