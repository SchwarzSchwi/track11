package p_database;

public class Cstudent_최선우_dto {
	private String id, name;
	private int age;
	public Cstudent_최선우_dto(String id, String name, int age) {
		this.id = id;
		this.name = name;
		this.age = age;
	}
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	
}
